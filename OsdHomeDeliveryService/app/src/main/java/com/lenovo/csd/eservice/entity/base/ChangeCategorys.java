package com.lenovo.csd.eservice.entity.base;

import java.util.List;

/**
 * Created by hankw on 16-8-19.
 */
public class ChangeCategorys extends BaseData {
    private String code;
    private String value;
    private List<ChangeTypes> change_types;
    private List<ReplacedPartsFlag> replaced_parts_flag;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public List<ChangeTypes> getChangeTypes() {
        return change_types;
    }

    public void setChangeTypes(List<ChangeTypes> change_types) {
        this.change_types = change_types;
    }

    public List<ReplacedPartsFlag> getReplacedPartsFlag() {
        return replaced_parts_flag;
    }

    public void setReplacedPartsFlag(List<ReplacedPartsFlag> replaced_parts_flag) {
        this.replaced_parts_flag = replaced_parts_flag;
    }
}
