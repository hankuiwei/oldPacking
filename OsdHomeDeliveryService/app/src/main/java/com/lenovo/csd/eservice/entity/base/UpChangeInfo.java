public class UpChangeInfo extends BaseData{

    private int id;
    private String change_code;
    private String machine_up_desc;
    private String machine_up_sn;
    private String change_type;
    private String machine_price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getChangeCode() {
        return change_code;
    }

    public void setChangeCode(String change_code) {
        this.change_code = change_code;
    }

    public String getMachineUpDesc() {
        return machine_up_desc;
    }

    public void setMachineUpDesc(String machine_up_desc) {
        this.machine_up_desc = machine_up_desc;
    }

    public String getMachineUpSn() {
        return machine_up_sn;
    }

    public void setMachineUpSn(String machine_up_sn) {
        this.machine_up_sn = machine_up_sn;
    }

    public String getChangeType() {
        return change_type;
    }

    public void setChangeType(String change_type) {
        this.change_type = change_type;
    }

    public String getMachinePrice() {
        return machine_price;
    }

    public void setMachinePrice(String machine_price) {
        this.machine_price = machine_price;
    }
}
